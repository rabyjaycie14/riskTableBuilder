          <li>Choose a risk from a catalogue. Predefined suggested figures will be set, but able to be edited</li>
          <li>Edit or delete the risk name, risk category, probability, and impact</li>
          <li>Sort table by risk or impact</li>
          <li>Check or uncheck if one has developed a corresponding risk information sheet (RIS)</li>
          <li>View the definitions of the impacts and risk categories</li>
